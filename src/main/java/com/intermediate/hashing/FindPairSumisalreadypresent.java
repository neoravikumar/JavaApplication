	public int solve(ArrayList<Integer> A) {

		Set<Integer> dataSet = new HashSet<Integer>();
		int N = null != A ? A.size() : 0;
		int counter=0;
		if (N >= 1 && N <= 1000) {

			for (int j : A) {
				if (j >= 1 && j <= (Math.pow(10, 6) + 5)) {
					dataSet.add(j);
				} else {
					return 0;
				}
			}
			int sum = 0;
			
			for (int i = 0; i < N; i++) {
				for (int j = 1 + i; j < N; j++) {
					sum = A.get(i) + A.get(j);
					if(dataSet.contains(sum)) {
						counter ++;
					}
				}
			}

		}

		return counter;
	}

